    "nvimdev/dashboard-nvim",
    event = "VimEnter",
    opts = function()
      local logo = [[
        ██╗     ██╗   ██╗██╗███╗   ██╗██╗   ██╗██╗███╗   ███╗
        ██║     ██║   ██║██║████╗  ██║██║   ██║██║████╗ ████║
